public class operaciones {
    public double suma;
    public double resta;
    public double producto;
    public double division;
    
    public void calcularsuma(double n1, double n2){
        suma = n1+n2;
    }
    
    public void calcularresta(double n1, double n2){
        resta = n1-n2;
    }
    
    public void calcularproducto(double n1, double n2){
        producto = n1*n2;
    }
    
    public void calculardivision(double n1, double n2){
        division = n1/n2;
    }
}
